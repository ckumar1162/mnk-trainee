# Functions with input

def greet_with_name(name):
    print(f"Hello {name}")
    print(f"How do you do {name}?")


greet_with_name("Jack Bauer")

def greet_with(name, location):
    print(f"hello {name}")
    print(f"what is it like in {location}")

greet_with("Rohit", "hubli")

